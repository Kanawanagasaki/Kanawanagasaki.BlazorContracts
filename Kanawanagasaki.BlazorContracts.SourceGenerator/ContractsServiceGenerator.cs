namespace Kanawanagasaki.BlazorContracts.SourceGenerator;

using Kanawanagasaki.BlazorContracts.SourceGenerator.Shared;
using Microsoft.CodeAnalysis;
using Microsoft.CodeAnalysis.CSharp;
using Microsoft.CodeAnalysis.CSharp.Syntax;

[Generator]
public class ContractsServiceGenerator : IIncrementalGenerator
{
    public void Initialize(IncrementalGeneratorInitializationContext context)
    {
        var handlersDeclarations = context.SyntaxProvider.CreateSyntaxProvider
        (
            static (node, _) => node is ClassDeclarationSyntax classDecl && classDecl.BaseList is not null,
            static (ctx, _) => ctx.SemanticModel.GetDeclaredSymbol((ClassDeclarationSyntax)ctx.Node)
        );

        var compilations = context.CompilationProvider
            .Combine(handlersDeclarations.Collect())
            .Select((tuple, _) =>
            {
                var (compilation, handlersSymbolds) = tuple;
                var ret = new List<HandlerMetadata>();

                foreach (var handler in handlersSymbolds)
                {
                    if (handler is null)
                        continue;
                    if (HandlerMetadata.TryCreate(handler, out var metadata) && metadata is not null)
                        ret.Add(metadata);
                }

                return ret;
            });

        context.RegisterSourceOutput(compilations, Execute);
    }

    private void Execute(SourceProductionContext context, List<HandlerMetadata> list)
    {
        var injectedServices = new Dictionary<string, string>();
        var withResponseContracts = new List<HandlerMetadata>();
        var noResponseContracts = new List<HandlerMetadata>();

        foreach (var meta in list)
        {
            foreach (var (injectedServiceType, _) in meta.HandlerInjectedServicesTypes)
            {
                if (!injectedServices.ContainsKey(injectedServiceType))
                    injectedServices[injectedServiceType] = $"_injected_service_" + (injectedServices.Count + 1);
            }

            if (meta.Contract.HasResponse)
                withResponseContracts.Add(meta);
            else
                noResponseContracts.Add(meta);
        }

        using var sw = new StringWriter();
        using var iw = new IndentedTextWriter(sw);
        iw.WriteLine("// <auto-generated/>");
        iw.WriteLine("namespace Kanawanagasaki.BlazorContracts;");
        iw.WriteLine();
        iw.WriteLine("public class ContractsService : Kanawanagasaki.BlazorContracts.IContractsService");
        iw.WriteLineAndIncrease("{");

        iw.WriteLine("private readonly Microsoft.Extensions.Logging.ILogger<ContractsService> _logger;");
        foreach (var kv in injectedServices)
            iw.WriteLine($"private readonly {kv.Key} {kv.Value};");

        iw.WriteLine();

        iw.WriteLineAndIncrease("public ContractsService(");
        iw.WriteLine(string.Join(",\n", injectedServices.Select(kv => $"{kv.Key} {kv.Value}").Concat(["Microsoft.Extensions.Logging.ILogger<ContractsService> logger"])));
        iw.DecreaseAndWriteLine(")");
        iw.WriteLineAndIncrease("{");
        iw.WriteLine("this._logger = logger;");
        foreach (var kv in injectedServices)
            iw.WriteLine($"this.{kv.Value} = {kv.Value};");
        iw.DecreaseAndWriteLine("}");

        iw.WriteLine();

        iw.WriteLine("public System.Threading.Tasks.Task<Kanawanagasaki.BlazorContracts.ContractResult> ProcessAsync(Kanawanagasaki.BlazorContracts.IContract req)");
        iw.WriteLineAndIncrease("{");
        iw.WriteLine("try");
        iw.WriteLineAndIncrease("{");
        iw.WriteLine("switch (req)");
        iw.WriteLineAndIncrease("{");
        for (int i = 0; i < noResponseContracts.Count; i++)
        {
            var meta = noResponseContracts[i];

            var constructorNames = meta.HandlerConstructorInjectedServicesTypes
                .Select(x => injectedServices.TryGetValue(x.TypeName, out var fieldName) ? fieldName : null)
                .Where(x => x is not null);

            iw.WriteLineAndIncrease($"case {meta.Contract.FullyQualifiedName} _contract_{i + 1}:");
            iw.Write($"var _handler_{i + 1} = new {meta.HandlerFullyQualifiedName}({string.Join(",", constructorNames)})");
            if (0 < meta.HandlerPropertiesInjectedServicesTypes.Count)
            {
                iw.WriteLine();
                iw.WriteLineAndIncrease("{");
                var isFirst = true;
                foreach (var injectProp in meta.HandlerPropertiesInjectedServicesTypes)
                {
                    if (!injectedServices.TryGetValue(injectProp.TypeName, out var fieldName))
                        continue;

                    if (isFirst)
                        isFirst = false;
                    else
                        iw.WriteLine(",");

                    iw.Write($"{injectProp.Name} = {fieldName}");
                }
                iw.WriteLineAndDecrease("");
                iw.Write("}");
            }
            iw.WriteLine(";");
            iw.WriteLine($"return _handler_{i + 1}.HandleAsync(_contract_{i + 1}, default);");
            iw.IndentLevel--;
        }
        iw.WriteLine("default: return System.Threading.Tasks.Task.FromResult(new Kanawanagasaki.BlazorContracts.ContractResult(405));");
        iw.DecreaseAndWriteLine("}");
        iw.DecreaseAndWriteLine("}");
        iw.WriteLine("catch (System.Exception e)");
        iw.WriteLineAndIncrease("{");
        iw.WriteLine("_logger.LogError(e, \"Failed to process a contract\");");
        iw.WriteLine("return System.Threading.Tasks.Task.FromResult(new Kanawanagasaki.BlazorContracts.ContractResult(500, \"Internal Server Error\"));");
        iw.DecreaseAndWriteLine("}");
        iw.DecreaseAndWriteLine("}");

        iw.WriteLine();

        iw.WriteLine("public async System.Threading.Tasks.Task<Kanawanagasaki.BlazorContracts.ContractResult<TResponse>> ProcessAsync<TResponse>(Kanawanagasaki.BlazorContracts.IContract<TResponse> req) where TResponse : class");
        iw.WriteLineAndIncrease("{");
        iw.WriteLine("try");
        iw.WriteLineAndIncrease("{");
        iw.WriteLine("switch (req)");
        iw.WriteLineAndIncrease("{");
        for (int i = 0; i < withResponseContracts.Count; i++)
        {
            var meta = withResponseContracts[i];

            var constructorNames = meta.HandlerConstructorInjectedServicesTypes
                .Select(x => injectedServices.TryGetValue(x.TypeName, out var fieldName) ? fieldName : null)
                .Where(x => x is not null);

            iw.WriteLineAndIncrease($"case {meta.Contract.FullyQualifiedName} _contract_{i + 1}:");
            iw.Write($"var _handler_{i + 1} = new {meta.HandlerFullyQualifiedName}({string.Join(",", constructorNames)})");
            if (0 < meta.HandlerPropertiesInjectedServicesTypes.Count)
            {
                iw.WriteLine();
                iw.WriteLineAndIncrease("{");
                var isFirst = true;
                foreach (var injectProp in meta.HandlerPropertiesInjectedServicesTypes)
                {
                    if (!injectedServices.TryGetValue(injectProp.TypeName, out var fieldName))
                        continue;

                    if (isFirst)
                        isFirst = false;
                    else
                        iw.WriteLine(",");

                    iw.Write($"{injectProp.Name} = {fieldName}");
                }
                iw.WriteLineAndDecrease("");
                iw.Write("}");
            }
            iw.WriteLine(";");

            iw.WriteLine($"var _result_{i + 1} = await _handler_{i + 1}.HandleAsync(_contract_{i + 1}, default);");
            iw.WriteLine($"return (Kanawanagasaki.BlazorContracts.ContractResult<TResponse>)(object)_result_{i + 1};");
            iw.IndentLevel--;
        }
        iw.WriteLine("default: return new Kanawanagasaki.BlazorContracts.ContractResult<TResponse>(405);");
        iw.DecreaseAndWriteLine("}");
        iw.DecreaseAndWriteLine("}");
        iw.WriteLine("catch (System.Exception e)");
        iw.WriteLineAndIncrease("{");
        iw.WriteLine("_logger.LogError(e, \"Failed to process a contract\");");
        iw.WriteLine("return new Kanawanagasaki.BlazorContracts.ContractResult<TResponse>(500, \"Internal Server Error\");");
        iw.DecreaseAndWriteLine("}");
        iw.DecreaseAndWriteLine("}");

        iw.DecreaseAndWriteLine("}");

        context.AddSource("ContractsService.g.cs", sw.ToString());
    }
}
